// ─── i18n — Multilingual UI (auto-detect, no runtime switch) ─────────────────

export type Lang = 'en' | 'de' | 'es' | 'fr' | 'pt' | 'it' | 'zh' | 'ja' | 'ru' | 'ar'

const SUPPORTED: readonly Lang[] = ['en', 'de', 'es', 'fr', 'pt', 'it', 'zh', 'ja', 'ru', 'ar']
const RTL_LANGS: readonly Lang[] = ['ar']

export const LOCALE_MAP: Record<Lang, string> = {
  en: 'en-US', de: 'de-DE', es: 'es-ES', fr: 'fr-FR', pt: 'pt-BR',
  it: 'it-IT', zh: 'zh-CN', ja: 'ja-JP', ru: 'ru-RU', ar: 'ar-SA',
}

function detectLang(): Lang {
  const navLangs: readonly string[] = navigator.languages?.length ? navigator.languages : [navigator.language]
  for (const nav of navLangs) {
    const code = nav.split('-')[0].toLowerCase()
    if ((SUPPORTED as readonly string[]).includes(code)) return code as Lang
  }
  return 'en'
}

export const currentLang: Lang = detectLang()
export const currentLocale: string = LOCALE_MAP[currentLang]
export const isRTL: boolean = (RTL_LANGS as readonly string[]).includes(currentLang)

